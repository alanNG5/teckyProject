function patternJet(txt) {

  let lines = txt.split('\n').slice(1, -1)

  let height = lines.length
  let width = Math.max(...lines.map(line => line.length))

  let cy = Math.round(height / 2)
  let cx = Math.round(width / 2)

  console.log({ cy, cx, width, height })

  let pattern = lines.flatMap((line, y) =>
    line.split('').map((char, x) => ({
      dx: x - cx,
      dy: y - cy,
      alive: char == 'x' || char == 'O',
    })),
  )

  return pattern
}

